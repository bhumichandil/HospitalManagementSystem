    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String pid = patientid.getText();
        int patientid = Integer.parseInt(pid);
        String pname = patientname.getText();
        String page = patientage.getText();
        int patientage  = Integer.parseInt(page);
        String doctorname = dname.getText();
        
        try{
            //load 
            Class.forName("com.mysql.cj.jdbc.Driver");
            //connection create
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/Hospital","root","272006");
            // statement
            String query  = "Insert INTO patient VALUES(?,?,?,?)";
            PreparedStatement pstm = con.prepareStatement(query);
            pstm.setInt(1,patientid);
            pstm.setString(2, pname);
            pstm.setInt(3, patientage);
            pstm.setString(4,doctorname);
            pstm.executeUpdate();
            con.close();
            JOptionPane.showMessageDialog(this,"Data inserted....");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,e);
        }
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

